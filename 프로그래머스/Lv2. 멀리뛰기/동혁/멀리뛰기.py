# 점화식
# 1 = 1 = 1가지
# 2 = 11,2 = 2가지
# 3 = 111,12,21 = 3가지
# 4 = 1111,22,211,121,112 = 5가지
# ...
def solution(n):
    # 1,2면 계산할 필요 없음
    if n < 3:
        return n

    li = [0] * (n + 1)
    li[1] = 1
    li[2] = 2

    # 점화식 계산(피보나치랑 같음)
    for i in range(3, n + 1):
        li[i] = li[i - 1] + li[i - 2]

    # 1234567를 나눈 나머지를 리턴하는 함수
    return li[n] % 1234567
print(solution(1))