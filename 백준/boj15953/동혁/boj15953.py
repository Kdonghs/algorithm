t = int(input())
one = [500, 300, 300, 200, 200, 200, 50, 50, 50, 50, 30, 30, 30, 30, 30, 10, 10, 10, 10, 10, 10]
two = [512, 256, 256, 128, 128, 128, 128, 64, 64, 64, 64, 64, 64, 64, 64, 32, 32, 32, 32, 32, 32, 32, 32, 32, 32, 32,
       32, 32, 32, 32, 32]
for i in range(t):
    a, b = map(int, input().split())
    sum = 0
    if 0 < a < 22:
        sum += one[a - 1]
    if 0 < b < 32:
        sum += two[b - 1]

